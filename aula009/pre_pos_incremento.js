/**
 * Pré e Pós Incremento
 */

let n = 10;

console.log(`[n = ${n}]`);

n++;
console.log("Pós Incremento n++: " + n);

console.log("Pré Incremento ++n: " + ++n);

console.log("\n");

n = 15;
console.log(`[n = ${n}]`);

n--;
console.log("Pós Incremento n--: " + n);

console.log("Pré Incremento --n: " + --n);