/**
 * Operador TypeOf (Unário)
 * Tipo da variável
 */

let v1 = 10;
let v2 = "10";
let v3 = v1 === v2;
let v4 = {name: "Giovani"};

console.log(`v1 = ${v1} - Tipo: ` + typeof v1);
console.log(`v2 = "${v2}" - Tipo: ` + typeof v2);
console.log(`v3 = ${v3} - Tipo: ` + typeof v3);
console.log(`v4 = ${v4} - Tipo: ` + typeof v4);