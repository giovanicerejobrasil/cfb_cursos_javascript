/**
 * Estrutura de Repetição / Iteração (WHILE || DO WHILE)
 */

let n = 10;

while (n < 10) {
    console.log("Curso JavaScript");
    n++;
}
console.log("Fim do programa");
console.log();

n = 10;

do {
    console.log("Curso JavaScript");
    n++;
} while (n < 10);
console.log("Fim do programa");